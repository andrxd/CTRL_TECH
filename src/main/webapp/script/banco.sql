	DESCRICAO VARCHAR(500),
	GENERO VARCHAR(20),
	FAIXAETARIA VARCHAR(20),
	PLATAFORMA VARCHAR(20),
	FABRICANTE VARCHAR(20),
	MARCA VARCHAR(30),
	GARANTIA VARCHAR(30),
	ARMAZENAMENTO VARCHAR(20),
